let number= Number(prompt("Enter number"))
switch(number) {
case 1 :
    window.alert("First day of week")
    break;

case 2:
    window.alert("Second day of week")
    break;

case 3:
    window.alert("Third day of week")
    break;

case 4:
    window.alert("Fourth day of week")
    break;

case 5:
    window.alert("Fifth day of week")
    break;

case 6:
    window.alert("Sixs day of week")
    break;
case 7:
    window.alert("WEEKENDD")
    break;
default :
    window.alert("Invalid input")
}